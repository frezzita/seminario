package classes;

public class ResultadoMensual {

    private int anio;
    private int mes;
    private String numero;
    private int piso;
    private double monto;
    private int edificioId;

    public ResultadoMensual() {
    }

    public int getAnio() {
        return anio;
    }

    public int getMes() {
        return mes;
    }

    public String getNumero() {
        return numero;
    }

    public int getPiso() {
        return piso;
    }

    public double getMonto() {
        return monto;
    }

    public int getEdificioId() {
        return edificioId;
    }

}
